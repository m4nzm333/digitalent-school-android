package com.unlistedi.mystudent;

public class Siswa {
    String id;
    String nama;
    String alamat;
    Double longitude;
    Double latitude;
    public Siswa(String id, String nama, String alamat, Double longitude, Double latitude){
        this.id = id;
        this.nama = nama;
        this.alamat = alamat;
        this.longitude = longitude;
        this.latitude = latitude;
    }
}
